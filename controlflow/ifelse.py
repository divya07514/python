# Control flow statements in python make use of colon and indentation

if True:
    print("True value printed.")

hungry = False
if hungry:
    print("Very hungry.")
else:
    print("Not Hungry.")

loc = "Mumbai"
if loc == "Pune":
    print("In Pune")
elif loc == "Delhi":
    print("In Delhi")
else:
    print("In Mumbai")
